#include "libft.h"
#include "mini.h"

void	ft_setenv(char **av, char ***env)
{
	if (!*(av + 1))
		env_print(*env);
	else if (!*(av + 2))
		env_add(*(av + 1), "''", env);
	else
		env_add(*(av + 1), *(av + 2), env);
}
